#include "../../includes/ft_printf.h"

int		ft_same(char *s1, char *s2)
{
	while (*s1 && *s2 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	if (*s1 == 0 && *s2 == 0)
		return (1);
	return (0);
}
